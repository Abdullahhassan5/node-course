UserRouter.get('/logout', (req, res) => { // logout the user 
  if (req.session) { // here session will will be destroyed  so that user will not havae the access
    req.session.destroy();
    res.clearCookie('session-id');
    res.redirect('/');
  }
  else {
    var err = new Error('You are not logged in!');
    err.status = 403;
    next(err);
  }
});
